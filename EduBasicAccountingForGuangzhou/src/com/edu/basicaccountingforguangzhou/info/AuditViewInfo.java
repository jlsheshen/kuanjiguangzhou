package com.edu.basicaccountingforguangzhou.info;

/**
 * 审核模块对每个控件的封装
 * 
 * @author lucher
 * 
 */
public class AuditViewInfo {

	private int id; // 控件在数据库对应的ID
	private int xAxis; // 控件横坐标
	private int yAxis; // 控件纵坐标
	private int width; // 控件占用宽度
	private int height; // 控件占用高度
	private String correctAnswer; // 正确答案
	private String wrongAnswer; // 错误答案
	private int mode;// 模式：0-显示正确答案，1-显示错误答案
	private int userAnswer;// 用户答案，练习模式：默认为0，选择为1，测试模式：默认为0，选对为1，选错为2
	private int inputType;// 初始化键盘,3位的代表年月日类型，2位代表金额类型
	private int flag;// 是否做过此题,0-未做过，1-做过

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getxAxis() {
		return xAxis;
	}

	public void setxAxis(int xAxis) {
		this.xAxis = xAxis;
	}

	public int getyAxis() {
		return yAxis;
	}

	public void setyAxis(int yAxis) {
		this.yAxis = yAxis;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public String getCorrectAnswer() {
		return correctAnswer;
	}

	public void setCorrectAnswer(String correctAnswer) {
		this.correctAnswer = correctAnswer;
	}

	public String getWrongAnswer() {
		return wrongAnswer;
	}

	public void setWrongAnswer(String wrongAnswer) {
		this.wrongAnswer = wrongAnswer;
	}

	public int getMode() {
		return mode;
	}

	public void setMode(int mode) {
		this.mode = mode;
	}

	public int getUserAnswer() {
		return userAnswer;
	}

	public void setUserAnswer(int userAnswer) {
		this.userAnswer = userAnswer;
	}

	public int getInputType() {
		return inputType;
	}

	public void setInputType(int inputType) {
		this.inputType = inputType;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

}
